#include<iostream>
using namespace std;

int main() {
	//比较运算符
	// 比较运算符----- == != > < >= <= 
	//表达式结果成立为1 反正为0；


	//  ==
	cout << (3 == 4) << endl;//括号增加优先级
	//  !=
	cout << (3 != 4) << endl;
	//  >
	cout << (3 > 4) << endl;
	//  <
	cout << (3 < 4) << endl;
	//  >=
	cout << (3 >= 4) << endl;
	//  <=
	cout << (3 <= 4) << endl;

	system("pause");
	return 0;
}